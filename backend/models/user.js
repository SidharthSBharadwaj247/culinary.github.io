const mongoose = require("mongoose");

const UserSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  favorites:{
    type : Array
  }
});

const UserList = new mongoose.model("Users", UserSchema);

module.exports = UserList;
